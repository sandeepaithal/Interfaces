package interfaces;

/**
 * This interface represents an Animal.
 * It defines methods for making sounds and moving.
 * 
 * @author C SANDEEP AITHAL
 */
public interface Animal {
    
    /**
     * Method to make a sound specific to the animal.
     */
    void makeSound();
    
    /**
     * Method to describe the movement of the animal.
     */
    void move();
}
